package Day2;

public class DataTypes {

	public static void main(String[] args)
	{
		
		// Numeric data type
		
		int a=100,b=200;
		System.out.println("The value of a is:"+a);
		System.out.println("The value of b is:"+b);
		System.out.println(a+b);
		System.out.println("The sum of a and b is:"+(a+b));
		
		byte z=125;
		System.out.println(z);
		
		short y=3435;
		System.out.println(y);
		
		long k=434555435435453l;
		System.out.println(k);
		
		// Decimal Number (double, float)
		
		float item_price=34.658778f;   //Five values are coming after the decimal,while proceeding with maximum number after decimal.
		System.out.println(item_price);
		
		double f=12534.23645276452;
		System.out.println(f);
		
		char grade='B';
		System.out.println(grade);
		
		String name="Kantipur";
		System.out.println(name);
		
	  // char ch='ABC';      //  Invalid -- Multiple character
	  // String ch='ABC';     //  Invalid -- For single quote
	  // String ch='K';       //  Invalid -- For single quote
		 String ch="D";      //   Valid
	     System.out.println(ch);
		
		boolean s1=true;           // Allows only true or false
		System.out.println(s1);
		
		//boolean z="false";   //  Invalid -- For double quote
		
		

	}

}
