package Day2;

public class Variables {

	public static void main(String[] args) 
	{
		
		
		  int a; 	                     //declaration 
		  a=100; 	                     //assignment 
		  System.out.println(a);  
		
		  
		  
		 int b = 130;                 // declaration+assignment
		 System.out.println(b);
		
		  int c= 100;
		  int d= 200;
		  int e= 300;
		  System.out.println(c+" "+d+" "+e);     //Print at a time all values (All the variables belongs to different data type)
		
		
		 int f,g,h;
		 f=150;
		 g=250;
		 h=350;
		 System.out.println(f+" "+g+" "+h);         //Print at a time all values (All the variables belongs to same data type)
		   
		int i=25,j=35,k=45;
		System.out.println(i+" "+j+" "+k);         //Print at a time all values(All the variables belongs to same data type)
		
		
		
		
		int l=123,m=456,n=789;
		System.out.println("The value of l is:"+l);
		System.out.println("The value of m is:"+m);
		System.out.println("The value of n is:"+n);            //Declared the values with classified ways
		
		int p=10,q=20,r=30;
		System.out.println(p+q+r);                 // Adding all values
		System.out.println(p+" "+q+" "+r);         // Print all values separately
		
		

	}

}
