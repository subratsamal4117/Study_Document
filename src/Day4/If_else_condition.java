package Day4;

public class If_else_condition {

	public static void main(String[] args) 
	{
		int person_age = 37;
		if (person_age<=21)        
		{
			System.out.println("Eligible for Vote");     //   If the condition is "true"
		}
		else
		{
			System.out.println("Not Eligible for Vote");     //   If the condition is "false"
	    }
	}
}